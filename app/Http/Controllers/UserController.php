    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     */
    public function destroy($id)
    {
        $demande = Demande::where('user_id', $id)->get();
        if(isset($demande[0]))
            $demande[0]->delete();
        $user = User::find($id);
        $user->delete();
        return redirect('/users');
    }
